#[test]
fn test_pandn_associativity() {
    // Test: (A PANDN B) PANDN C vs A PANDN (B | C) - different operations but test structure
    let code = vec![
        0x0f, 0x6f, 0x04, 0x25, 0x00, 0x20, 0x00, 0x00,
        0x0f, 0x6f, 0x0c, 0x25, 0x08, 0x20, 0x00, 0x00,
        0x0f, 0xdf, 0xc1,                               // MM0 = ~MM0 & MM1
        0x0f, 0x6f, 0x14, 0x25, 0x10, 0x20, 0x00, 0x00,
        0x0f, 0xdf, 0xc2,                               // MM0 = ~MM0 & MM2
        0x0f, 0x7f, 0x04, 0x25, 0x18, 0x20, 0x00, 0x00,
        0xf4,
    ];

    let (mut vcpu, mem) = setup_vm(&code, None);

    write_mm_via_mem(&mem, 0x2000, 0xFFFFFFFF00000000);
    write_mm_via_mem(&mem, 0x2008, 0xFFFF0000FFFF0000);
    write_mm_via_mem(&mem, 0x2010, 0xFF00FF00FF00FF00);

    run_until_hlt(&mut vcpu).unwrap();

    let result = read_mem_at_u64(&mem, 0x2018);
    // First: ~0xFFFFFFFF00000000 & 0xFFFF0000FFFF0000 = 0x00000000FFFFFFFF & 0xFFFF0000FFFF0000 = 0x0000000000000000 (wrong)
    // Let me recalc: ~0xFFFFFFFF00000000 = 0x00000000FFFFFFFF, & 0xFFFF0000FFFF0000 = 0x00000000FFFF0000
    // Second: ~0x00000000FFFF0000 = 0xFFFFFFFF0000FFFF, & 0xFF00FF00FF00FF00 = 0xFF00FF000000FF00
    assert_eq!(result, 0xFF00FF000000FF00, "PANDN: chained operations");
}
