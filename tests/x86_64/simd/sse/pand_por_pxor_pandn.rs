use crate::common::*;
use vm_memory::{Bytes, GuestAddress};

// PAND/POR/PXOR/PANDN - Packed Logical Operations (SSE2)
//
// Performs bitwise logical operations on 128-bit packed integers.
// These are bitwise operations operating on all bits regardless of data type.
//
// PAND:  Bitwise AND  - DEST := DEST AND SRC
// POR:   Bitwise OR   - DEST := DEST OR SRC
// PXOR:  Bitwise XOR  - DEST := DEST XOR SRC
// PANDN: Bitwise AND NOT - DEST := (NOT DEST) AND SRC
//
// Opcodes (SSE2 - 128-bit XMM):
// 66 0F DB /r      PAND xmm1, xmm2/m128    - Bitwise AND
// 66 0F EB /r      POR xmm1, xmm2/m128     - Bitwise OR
// 66 0F EF /r      PXOR xmm1, xmm2/m128    - Bitwise XOR
// 66 0F DF /r      PANDN xmm1, xmm2/m128   - Bitwise AND NOT

const ALIGNED_ADDR: u64 = 0x3000;
const ALIGNED_ADDR2: u64 = 0x3100;

// ============================================================================
// PAND Tests - Bitwise AND
// ============================================================================

#[test]
fn test_pand_all_ones() {
    // 0xFF AND 0xFF = 0xFF
    let code = [0x48, 0xb8];
    let mut full_code = code.to_vec();
    full_code.extend_from_slice(&ALIGNED_ADDR.to_le_bytes());
    full_code.extend_from_slice(&[0x48, 0xbb]);
    full_code.extend_from_slice(&ALIGNED_ADDR2.to_le_bytes());
    full_code.extend_from_slice(&[
        0x66, 0x0f, 0x6f, 0x00, // MOVDQA XMM0, [RAX]
        0x66, 0x0f, 0x6f, 0x0b, // MOVDQA XMM1, [RBX]
        0x66, 0x0f, 0xdb, 0xc1, // PAND XMM0, XMM1
        0xf4, // HLT
    ]);

    let (mut vcpu, mem) = setup_vm(&full_code, None);
    mem.write_slice(&[0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF], GuestAddress(ALIGNED_ADDR)).unwrap();
    mem.write_slice(&[0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF], GuestAddress(ALIGNED_ADDR2)).unwrap();
    run_until_hlt(&mut vcpu).unwrap();
}

